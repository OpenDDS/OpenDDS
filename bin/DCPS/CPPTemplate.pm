  for (; it != the_end; ++it)
  {
    handle = it->second;
    DDS::ReturnCode_t const status = read_instance(received_data,
                                                   info_seq,
                                                   max_samples,
                                                   handle,
                                                   sample_states,
                                                   view_states,
                                                   instance_states);
    if (status != ::DDS::RETCODE_NO_DATA)
    {
      return status;
    }
  }

  return ::DDS::RETCODE_NO_DATA;
}

